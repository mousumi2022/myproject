public class AnimalInheritance {
			public void eat() {
			System.out.println("Inside animals eat");
		}
		public void bite() {
			System.out.println("Inside animals bite");
	}
		
	}
class lion extends AnimalInheritance{//Extends is used in Inheritance
	public void roar() {
		System.out.println("Lion Roar");
	}
	
}
	
		
	


