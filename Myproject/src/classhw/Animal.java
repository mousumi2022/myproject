package classhw;

public class Animal  {              
		
	public void eat() {
		System.out.println("Inside animals eat");
	}
	public void bite() {
		System.out.println("Inside animals bite");
}
}
class Lion extends Animal{
	                                           
	public void roar() {                        
		System.out.println("lions roar");
	}
	public void bite() {
		System.out.println("Inside lions bite");
	
}

}